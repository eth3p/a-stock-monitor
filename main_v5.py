# 初始化数据库
def init_db():
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS stocks (
            code TEXT PRIMARY KEY,
            name TEXT,
            current_price REAL,
            change_pct REAL,
            volume BIGINT,
            amount BIGINT,
            market TEXT,
            update_time TEXT
        )
    ''')
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS price_history (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            code TEXT,
            price REAL,
            change_pct REAL,
            volume BIGINT,
            market TEXT,
            timestamp TEXT
        )
    ''')
    conn.commit()
    conn.close()

# 保存数据
def save_stock_data(stocks):
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    for s in stocks:
        cursor.execute('''
            INSERT OR REPLACE INTO stocks 
            (code, name, current_price, change_pct, volume, amount, market, update_time)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?)
        ''', (s['code'], s['name'], s['current_price'], s['change_pct'], 
              int(s.get('volume', 0)), int(s.get('amount', 0)), s.get('market', '股票'), now))
        
        cursor.execute('''
            INSERT INTO price_history (code, price, change_pct, volume, market, timestamp)
            VALUES (?, ?, ?, ?, ?, ?)
        ''', (s['code'], s['current_price'], s['change_pct'], int(s.get('volume', 0)), s.get('market', '股票'), now))
    
    conn.commit()
    conn.close()

# 检测异常波动
def detect_opportunities():
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    
    now = datetime.now()
    five_min_ago = (now - timedelta(minutes=5)).strftime("%Y-%m-%d %H:%M:%S")
    
    cursor.execute('''
        SELECT code, name, current_price, change_pct, volume, update_time, market 
        FROM stocks 
        WHERE ABS(change_pct) > 3 
        AND update_time > ?
    ''', (five_min_ago,))
    
    results = cursor.fetchall()
    conn.close()
    
    opportunities = []
    for row in results:
        code, name, price, pct, volume, time, market = row
        score = 50
        
        if pct > 0:
            score += min(int(pct * 5), 30)
        else:
            score -= min(int(abs(pct) * 5), 30)
        
        if volume > 1000000:
            score += 10
        
        if pct > 5:
            reason = "涨幅>5%，放量突破"
        elif pct > 3 and volume > 500000:
            reason = "涨幅>3%，放量活跃"
        elif pct < -5:
            reason = "跌幅>5%，超卖机会"
        else:
            reason = "异常波动"
        
        if score >= 60 or (score >= 40 and pct < -3):
            opportunities.append({
                "code": code,
                "name": name,
                "price": price,
                "change_pct": pct,
                "score": min(max(score, 0), 100),
                "reason": reason,
                "market": market
            })
    
    return opportunities

